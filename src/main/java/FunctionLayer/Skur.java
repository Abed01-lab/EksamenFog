package FunctionLayer;

public class Skur {

    int bredde;
    int længde;

    public Skur(int bredde, int længde) {
        this.bredde = bredde;
        this.længde = længde;
    }

    public int getBredde() {
        return bredde;
    }

    public int getLængde() {
        return længde;
    }
}
